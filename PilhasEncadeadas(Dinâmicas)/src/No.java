public class No {
	
	int elemento;
	private int proximo;
	
	public No(int topo) {
		this.elemento = (int)elemento;
		this.proximo = topo;
	}
	
	public Object getElemento(int max) {
		return elemento;
	}
	public void setElemento(int elemento) {
		this.elemento = elemento;
	}
	public int getProximo() {
		return proximo;
	}
	public void setProximo(int proximo) {
		this.proximo = proximo;
	}
	
}
